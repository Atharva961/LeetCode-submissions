class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int>arr, int n, long long s)
    {
        long long beg = 0, end = 0, sum = 0;
        vector<int> ans = {-1};
        while(end<n)
        {
            sum+=arr[end];
            while(sum>s && beg<end)
            {
                sum-=arr[beg];
                beg++;
            }
            if(sum==s)return {beg+1, end+1};
            end++;
        }
        return ans;
    }
};

