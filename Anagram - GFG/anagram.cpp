class Solution
{
    public:
    //Function is to check whether two strings are anagram of each other or not.
    bool isAnagram(string a, string b){
        
        // Your code here
        vector<int> h(26, 0);
        for(char i:a)h[i-'a']++;
        for(char i:b)h[i-'a']--;
        for(int i:h)if(i!=0)return false;
        return true;
    }

};

